//Funções declarativas
function funcao () {
    console.log("Tudo certo jovem!")
}
funcao();

//Expressões de funções (com ou sem nomeação)
var funcao = function funcao() {
    console.log(funcao)
}
funcao();

//Arrow function
var funcao = () => {
    console.log(funcao)
}
funcao();

